package it.polito.tdp.gosales.model;

public class Connessa {
	
	private int numConn;
	private int peso;
	public Connessa(int numConn, int peso) {
		super();
		this.numConn = numConn;
		this.peso = peso;
	}
	public int getNumConn() {
		return numConn;
	}
	public void setNumConn(int numConn) {
		this.numConn = numConn;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	

}
